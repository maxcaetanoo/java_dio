public class App {
    public static void main(String[] args) throws Exception {
        // Tipos primitivos:

        // Representação de variavel tipo inteiro
        int num = 10;
        num++; // Representação de incremento e mudança da variavel

        System.out.println(num); // Exibição da variavel.

        // Representação de constante tipo double
        final double SALARIO = 1400.25;

        System.out.println(SALARIO); // Exibição da constante
    }
}
