public class App {
    public static void main(String[] args) throws Exception {
        // Concatenação
        String nomeCompleto = "Max" + "Caetano";
        System.out.println(nomeCompleto);

        // Inversão de valores
        int numero = 5;
        System.out.println(-numero); // Valor invertido
        System.out.println(numero); // Valor ainda continua o mesmo, só foi substituido por um negativo para ser
                                    // exibido anteriormente

    }
}
